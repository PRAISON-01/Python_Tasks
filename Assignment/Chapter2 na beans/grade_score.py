grade = int(input("Enter your grade score (0-100) : "))

if grade >= 90:
    print("Excellent you got an 'A'")
elif grade >= 80:
    print("Very good you got a 'B'")
elif grade >= 70:
    print("Good you got a 'C'")
elif grade >= 60:
    print("Fair you got a 'D'")
elif grade >= 50:
    print("Poor you got an 'E'")
else:
    print("Very poor you got an 'F'")
    
